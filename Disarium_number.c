#include<stdio.h>
#include<math.h>
int main()
{
    int a,r,q,s=0,t,c=0,k=0;
    scanf("%d",&a);
    t=a;
    while(a)
    {
        r=a%10;
        s=s*10+r;
        a=a/10;
    }
    while(s)
    {
        q=s%10;
        c++;
        k=k+(pow(q,c));
        s=s/10;
    }
    if(k==t)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}